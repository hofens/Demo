package com.example.demo.scroll;

public class DateBean {
    private String date;
    private String time;

    public DateBean(String date, String time) {
        this.date = date;
        this.time = time;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}